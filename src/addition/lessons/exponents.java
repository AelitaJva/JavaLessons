public class exponents {
    public static void main(String[] args) {

        double exponent1 = 6;
        double exponent2 = Math.pow(4, 2);
        int exponent3 = (int) Math.pow(5, 2);

        System.out.println(Math.pow(exponent1, 2));
        System.out.println(exponent2);
        System.out.println(exponent3);
    }
}
