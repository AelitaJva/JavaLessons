package com.java.class04;

public class ArithmeticOperators {
    public static void main(String[] args) {
        System.out.println(10 + 20);
        System.out.println(10 - 20);
        System.out.println(10 * 20);
        System.out.println(10 / 20);

        // Modulo
        // 14 % 10 = 1.4 (4 -> is reminder, remaining part)
        // when we can't easily divide number
        System.out.println(14 % 10);

        // when we have a modulo with 2 we can  check if given number odd or even
        //even
        //odd

        // when we have a modulo with 10 we get last digit from the number
        System.out.println(8 % 10);

        // / -> first digit
        System.out.println(41 / 10);

        // % -> last digit
        System.out.println(41 % 10);

        // when we have a modulo with 2 we can  check if given number odd or even



    }
}
