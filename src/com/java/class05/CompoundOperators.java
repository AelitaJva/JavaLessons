package com.java.class05;

public class CompoundOperators {
    public static void main(String[] args) {

        int num1 = 10;
        num1+=5; // num1 = num1+5;
        System.out.println(num1);

        int num2 = 12;
        System.out.println(num2 + 8); //20

        num2*=2; //24
        System.out.println(num2);
    }
}
