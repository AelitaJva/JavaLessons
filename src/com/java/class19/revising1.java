package com.java.class19;

public class revising1 {
    public static void main(String[] args) {
        // output from 10 to 1

        // ex: 1
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }

        System.out.println("===============");

        // ex 2:
        for ( int j = 1; j <= 10; j++) {
            System.out.println(j + 1);
        }
    }
}
